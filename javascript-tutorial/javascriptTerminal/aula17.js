// Operadores em JavaScript

// Operadores aritmédicos

let num1 = 3;
let num2 = 2;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % num2);
console.log(++num1);
console.log(--num2);

// Operador de atribuição

let a = 5;
a += 5; // +=, -=, *=, /=, %=
console.log(a);